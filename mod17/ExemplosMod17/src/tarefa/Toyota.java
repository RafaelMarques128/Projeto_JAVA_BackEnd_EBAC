package tarefa;

class Toyota {
    private String modelo;

    public Toyota(String modelo) {
        this.modelo = modelo;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    @Override
    public String toString() {
        return "Toyota " + modelo;
    }
}
