package tarefa;

class Ford {
    private String modelo;

    public Ford(String modelo) {
        this.modelo = modelo;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    @Override
    public String toString() {
        return "Ford " + modelo;
    }
}
